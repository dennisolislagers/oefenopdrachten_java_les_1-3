package nl.novi.opdrachten.whilelussen;

public class Teller {
    public static void main(String[] args) {
        int teller = 0;
//        De while loop die doorloopt totdat de teller op 10 staat
        while (teller <= 10){
//            De teller uitprinten.
            System.out.println("De teller staat nu op: " + teller);
//            De teller moet met 1 optellen als de whileloop loopt.
            teller++;
        }
        /*
        Maak een while-lus.
        Deze while-lus moet net zo lang lopen tot en met de teller op 10 staat.
        Elke keer wanneer de teller wordt opgehoogd moet deze uitgeprint worden.
         */
    }
}
